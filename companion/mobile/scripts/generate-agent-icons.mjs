// Regenerates src/lib/agentIcons.ts from the desktop app's agent logo SVGs.
// Run: node scripts/generate-agent-icons.mjs
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const assetsDir = join(here, '../../../ZenttyResources/Assets.xcassets');

// Desktop imageset name (minus AgentIcon prefix) → camelCase key used in the app.
const ICONS = [
  'Amp', 'ClaudeCode', 'Codex', 'Copilot', 'Cursor', 'Droid', 'Gemini',
  'Grok', 'Hermes', 'Kimi', 'Mistral', 'OpenCode', 'Pi', 'Zentty',
];

// Make a logo tint-ready: force the ROOT <svg> fill to currentColor (even when
// it is `fill="none"`), and drop hardcoded dark fills on inner paths so they
// inherit it. Any inner `fill="none"` (structural, non-drawn) is left intact.
function tintReady(svg) {
  let out = svg.replace(/<title>.*?<\/title>/gi, '');
  const openTag = out.match(/<svg\b[^>]*>/i)[0];
  const tintedTag = /\sfill="[^"]*"/i.test(openTag)
    ? openTag.replace(/\sfill="[^"]*"/i, ' fill="currentColor"')
    : openTag.replace(/<svg\b/i, '<svg fill="currentColor"');
  const inner = out
    .slice(out.indexOf(openTag) + openTag.length)
    .replace(/\s+fill="#(?:000|000000|222|222222)"/gi, '');
  return (tintedTag + inner).replace(/\s+/g, ' ').replace(/> </g, '><').trim();
}

const entries = ICONS.map((name) => {
  const file = join(assetsDir, `AgentIcon${name}.imageset`, `AgentIcon${name}.svg`);
  const key = name.charAt(0).toLowerCase() + name.slice(1);
  return [key, tintReady(readFileSync(file, 'utf8'))];
});

const body = entries.map(([k, v]) => `  ${k}: ${JSON.stringify(v)},`).join('\n');
const ts = `// AUTO-GENERATED by scripts/generate-agent-icons.mjs — do not edit by hand.
// Source: ZenttyResources/Assets.xcassets/AgentIcon*.imageset (the desktop app's
// agent logos), transformed to tint via currentColor. Regenerate when logos change.

/** Tint-ready SVG markup per agent, keyed by a stable slug. */
export const AGENT_ICON_SVG = {
${body}
} as const;

export type AgentIconKey = keyof typeof AGENT_ICON_SVG;
`;
writeFileSync(join(here, '../src/lib/agentIcons.ts'), ts);
console.log(`wrote src/lib/agentIcons.ts (${entries.length} icons)`);
