import Foundation

/// A loss-preserving representation of an arbitrary JSON value.
///
/// Used for wire fields whose shape is deliberately open — adapter-specific
/// `toolInput` payloads on transcript entries, and the payload of an
/// unsupported (unknown-`type`) envelope that must be carried through a
/// decode/re-encode cycle without losing information.
enum CompanionJSONValue: Codable, Equatable, Sendable {
    case null
    case bool(Bool)
    case int(Int)
    case double(Double)
    case string(String)
    case array([CompanionJSONValue])
    case object([String: CompanionJSONValue])

    init(from decoder: Decoder) throws {
        let container = try decoder.singleValueContainer()
        if container.decodeNil() {
            self = .null
            return
        }
        // Order matters: Bool before Int so `true`/`false` never coerce to a
        // number, and Int before Double so integral values stay integral.
        if let bool = try? container.decode(Bool.self) {
            self = .bool(bool)
        } else if let int = try? container.decode(Int.self) {
            self = .int(int)
        } else if let double = try? container.decode(Double.self) {
            self = .double(double)
        } else if let string = try? container.decode(String.self) {
            self = .string(string)
        } else if let array = try? container.decode([CompanionJSONValue].self) {
            self = .array(array)
        } else if let object = try? container.decode([String: CompanionJSONValue].self) {
            self = .object(object)
        } else {
            throw DecodingError.dataCorruptedError(
                in: container,
                debugDescription: "Unsupported JSON value"
            )
        }
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.singleValueContainer()
        switch self {
        case .null:
            try container.encodeNil()
        case .bool(let value):
            try container.encode(value)
        case .int(let value):
            try container.encode(value)
        case .double(let value):
            try container.encode(value)
        case .string(let value):
            try container.encode(value)
        case .array(let value):
            try container.encode(value)
        case .object(let value):
            try container.encode(value)
        }
    }
}
